# VARIABLES EN PYTHON
# --------------------
# En Python, una variable es un nombre que se usa para guardar un valor en la memoria del computador.
# Piensa en una variable como una caja con una etiqueta: la etiqueta es el nombre de la variable y dentro de la caja está el valor que contiene.

# Ejemplo de declaración de variables:
nombre = "Juan"      # Guarda un texto (string)
edad = 25            # Guarda un número entero (int)
altura = 1.75        # Guarda un número decimal (float)

# Tipos de variables:
# nombre → tipo str (cadena de texto)
# edad   → tipo int (número entero)
# altura → tipo float (número decimal)

# -----------------------------------------------------------------
# EJEMPLO PRÁCTICO: Diario de un Astronauta
# -----------------------------------------------------------------

# Declaramos las variables del astronauta
nombre_astronauta = "Juan"
edad_astronauta = 32
destino = "Marte"

# Mostramos información usando las variables y f-strings
print(f"Hola, soy {nombre_astronauta}, tengo {edad_astronauta} años y mi próximo destino es {destino}.")

combustible = 100   # Porcentaje de combustible restante
velocidad = 50      # Velocidad en km/s

print(f"Estoy navegando a {velocidad} km/s con {combustible}% de combustible restante hacia {destino}.")

